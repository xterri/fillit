#include "fillit.h"

void	insert_newlines(char *map, int n)
{
	int		i;
	int		pos;

	i = 0;
	pos = n;
	while (i < n)
	{
		pos = n * (i + 1) + i;
		map[pos] = '\n';
		i++;
	}
}
