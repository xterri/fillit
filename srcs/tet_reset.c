#include "fillit.h"

void	tet_reset(t_list *trav)
{
	int		i;
	int		tmp;

	i = 0;
	tmp = trav->offset[0];
	while (i < ROW_SIZE)
	{
		trav->offset[i] -= tmp;
		++i;
	}
}
